public class Operators {
        public static void main(String[] args) {


            int a = 12, b = 5;


            System.out.println("a + b = " + (a + b));


            System.out.println("a - b = " + (a - b));


            System.out.println("a * b = " + (a * b));


            System.out.println("a / b = " + (a / b));


            System.out.println("a % b = " + (a % b));

            int c = 5;
            int var;


            var = c;
            System.out.println("var using =: " + var);


            var += c;
            System.out.println("var using +=: " + var);


            var *= c;
            System.out.println("var using *=: " + var);




            System.out.println(a == b);


            System.out.println(a != b);


            System.out.println(a > b);


            System.out.println(a < b);


            System.out.println(a >= b);


            System.out.println(a <= b);




        }
}


