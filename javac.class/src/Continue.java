public class Continue {
    public static void main(String[] args) {
        for (int i = 5; i <= 12; i++) {
            if (i == 8) {
                continue;

            }
            System.out.println(i);

            System.out.println("next continue");



        }
        int i = 6;
        while (i < 14) {


            System.out.println(i);
            i++;

            if (i == 8);
            continue;
        }
    }
}

